package ru.vstp.ubm.model;

public class DocumentForm {
    private int docId;
    private String docNote;

    public int getDocId() {
        return docId;
    }

    public void setDocId(int docId) {
        this.docId = docId;
    }

    public String getDocNote() {
        return docNote;
    }

    public void setDocNote(String docNote) {
        this.docNote = docNote;
    }
}
